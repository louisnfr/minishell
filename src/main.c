#include "minishell.h"

int	main(int ac, char **av, char **envp)
{
	char	*input;

	(void)av;
	if (ac != 1)
		exit(EXIT_FAILURE);
	g_env = create_env(envp);
	while (1)
	{
		prompt();
		get_next_line(0, &input);
		parse(input);
	}
	return (0);
}
